let intervalId = null;

module.exports.onLoad = function ({ api }) {
  const fs = require("fs");
  const path = __dirname + "/cache/autosent_status.json";

  // ‡¶≤‡ßã‡¶° ‡¶ï‡¶∞‡¶æ‡¶∞ ‡¶∏‡¶Æ‡¶Ø‡¶º ‡¶Ü‡¶ó‡ßá‡¶∞ ‡¶∏‡ßç‡¶ü‡ßç‡¶Ø‡¶æ‡¶ü‡¶æ‡¶∏ ‡¶ö‡ßá‡¶ï ‡¶ï‡¶∞‡¶æ
  if (fs.existsSync(path)) {
    try {
      const data = JSON.parse(fs.readFileSync(path, "utf8"));
      isRunning = data.isRunning || false;
    } catch (e) {}
  }

  if (isRunning) {
    startSending(api);
  }
};

function startSending(api) {
  if (intervalId) clearInterval(intervalId);

  intervalId = setInterval(() => {
    if (!isRunning) return;

    const now = new Date(Date.now() + 6 * 60 * 60 * 1000); // +6 hours BD time
    const currentTime = now.toLocaleTimeString("en-US", {
      hour: "numeric",
      minute: "2-digit",
      second: "2-digit",
      hour12: true
    });

    const found = messages.find(item => item.timer === currentTime);
    if (found) {
      const randomMsg = found.message[Math.floor(Math.random() * found.message.length)];

      global.data.allThreadID.forEach(threadID => {
        // ‡¶ö‡ßá‡¶ï ‡¶ï‡¶∞‡¶æ ‡¶Ø‡ßá ‡¶è‡¶á ‡¶•‡ßç‡¶∞‡ßá‡¶°‡ßá ‡¶ö‡¶æ‡¶≤‡ßÅ ‡¶Ü‡¶õ‡ßá ‡¶ï‡¶ø ‡¶®‡¶æ
        if (global.data.autosent?.[threadID] !== false) {
          api.sendMessage(randomMsg, threadID);
        }
      });
    }
  }, 1000);
}

function saveStatus() {
  const fs = require("fs");
  const path = __dirname + "/cache/autosent_status.json";
  fs.writeFileSync(path, JSON.stringify({ isRunning }), "utf8");
}

